export function getWritingNiches(): string[] {
    return Object.keys(PATTERNS);
}

export function getWritingAlgorithmStats(): { niches: number; totalTemplates: number } {
    const niches = Object.keys(PATTERNS).length;
    const totalTemplates = Object.values(PATTERNS).reduce((acc, pack) => {
        return acc
            + pack.headings.length
            + pack.descriptions.length
            + pack.links.length
            + pack.buttons.length
            + pack.faqQuestions.length
            + pack.faqAnswers.length
            + pack.messengerMessages.length
            + pack.productNames.length
            + pack.productDescriptions.length;
    }, 0);

    return { niches, totalTemplates };
}

export interface SuggestionContext {
    city?: string;
    profession?: string;
    company_name?: string;
    business_type?: string;
    creation_type?: string;
    subject?: string;
    course_name?: string;
    specialization?: string;
    // New niche contexts
    beauty_name?: string;
    beauty_type?: string;
    store_name?: string;
    product_category?: string;
}

/**
 * Personalize a template string by replacing placeholders with context values.
 */
function personalize(template: string, context: SuggestionContext): string {
    let result = template;
    const entries = Object.entries(context);
    
    entries.forEach(([key, value]) => {
        if (value) {
            const regex = new RegExp(`{${key}}`, 'g');
            result = result.replace(regex, value);
        }
    });

    // Clean up any remaining tags with defaults to avoid broken UI
    return result.replace(/{[a-z_]+}/g, (match) => {
        const key = match.slice(1, -1);
        const defaults: Record<string, string> = {
            city: "вашем городе",
            profession: "специалист",
            company_name: "наша компания",
            business_type: "наш бизнес",
            creation_type: "творческой сфере",
            subject: "новым навыкам",
            course_name: "обучающий курс",
            specialization: "ваш эксперт",
            beauty_name: "наша студия",
            beauty_type: "салон красоты",
            store_name: "наш магазин",
            product_category: "товаров"
        };
        return defaults[key] || "";
    });
}

/**
 * Main entry point for Smart-Writing suggestions.
 */
export function getWritingSuggestions(niche: string, context: SuggestionContext = {}): SuggestionPack {
    const pack = PATTERNS[niche] || PATTERNS.general;
    
    return {
        headings: pack.headings.map(h => personalize(h, context)),
        descriptions: pack.descriptions.map(d => personalize(d, context)),
        links: pack.links.map(l => personalize(l, context)),
        buttons: pack.buttons.map(b => personalize(b, context)),
        faqQuestions: pack.faqQuestions.map(q => personalize(q, context)),
        faqAnswers: pack.faqAnswers.map(a => personalize(a, context)),
        messengerMessages: pack.messengerMessages.map(m => personalize(m, context)),
        productNames: pack.productNames.map(n => personalize(n, context)),
        productDescriptions: pack.productDescriptions.map(d => personalize(d, context))
    };
}

export type SuggestionType = 'heading' | 'description' | 'link' | 'button' | 'faq_question' | 'faq_answer' | 'messenger_message' | 'product_name' | 'product_description';

